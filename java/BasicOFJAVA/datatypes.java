package BasicOFJAVA;

public class datatypes {
    public static void main(String[] args) {
        int num1 = 5;
        System.out.println("This is an Integer "+ num1);

        float f =5.67f;
        System.out.println("This is a float "+ f);

        double d = 5.66;
        System.out.println("This is a double " +d);

        long l = 587789l;
        System.out.println("this is long " + l);

        boolean b =true;
        System.out.println("this is boolean " +b);
        
        char c ='b';
        System.out.println("this is character " + c);
    }
    
}

