
public class Arraybasic1 {


    public static void main(String[] args) {
        // 1st way to intialise the array when we know the variable
        int anArray[] = { 4, 3, 6 };
        // to access the elementof array by using index
        System.out.println("elementof an array " + anArray[0]);

        for (int i = 0; i < 3; i++) {
            System.out.println("elementof an array " + anArray[i]);
        }

    }

}
